"""
part_mapper.py — утилиты маппинга названий элементов между сервисами.

Используется оркестратором для:
  1. Определения каким элементам можно считать стоимость ремонта
  2. Формирования итогового отчёта с нормализованными названиями
"""

from __future__ import annotations
from typing import Optional

# Элементы для которых НЕ считаем стоимость ремонта
NON_REPAIRABLE = {
    "Шина",
    "Колесный диск",
    "Номерной знак",
    "Эмблема",
    "Крышка бензобака",
    "ПТФ",
}

# Все классы YOLO — для валидации
